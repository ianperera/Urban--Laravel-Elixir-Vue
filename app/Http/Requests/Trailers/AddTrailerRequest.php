<?php

namespace App\Http\Requests\Trailers;

use Illuminate\Foundation\Http\FormRequest;
use App\Http\Requests\Request;
use App\Validators\Validator;
use App\Models\Trailer;


class AddTrailerRequest extends Request
{
    /**
     * Overwrite laravel's method, define custom validator
     */
    public function validate()
    {
        $request = $this->all();
        array_walk_recursive($request, function (&$item, $key) {
            if ($item === 'null') $item = null;
        });
        Request::merge($request);
        $this->validator = Validator::make($request)->addRules(Trailer::$rules);

        $this->rules();
        $this->runValidator();
    }

    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize()
    {
        return true;
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        $this->validator->append('name', 'required');
        $this->validator->append('deliver_capacity', 'required');
        return $this->rules;
    }
}
